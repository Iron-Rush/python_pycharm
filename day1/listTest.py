l = [1,2,4,5,9,9,11]
# 序列元素的下标从0开始
# tuple元素不可赋值
# 基本样式[下限:上限:步长]
print(l[1])
# 2
print(l[0:3:2])
# [1, 4]
print(l)
# [1, 2, 4, 5, 9, 9, 11]

# list元素是可以改变的
l[1] = 100
print(l)
# [1, 100, 4, 5, 9, 9, 11]

# insert在list指定位置插入项目
l.insert(1,"新来的")
print(l)
# [1, '新来的', 100, 4, 5, 9, 9, 11]

# append在List末尾添加一个项目
l.append("排队队")
print(l)
# [1, '新来的', 100, 4, 5, 9, 9, 11, '排队队']

